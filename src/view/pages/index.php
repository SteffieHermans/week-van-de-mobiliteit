      <h3 class="actie-title">Auto Vrije Zondag</h3>
      <p>Geef de straten van jouw stad of gemeente een dagje autorust! Deze hoogdag van de Week van de Mobiliteit geeft fietsers, wandelaars, sporters, ouderen en spelende kinderen vrij spel. De straat is meer dan de weg van A naar B, en op Autovrije Zondag kan iedereen daarvan proeven.</p>
      <a class="btn small-button" href="#">Meer over Auto Vrije Zondag</a>
    </article>
    <article class="event-day donderdag">
      <p class="event-date">Donderdag 20/9</p>
      <h3 class="actie-title">Car Free Day</h3>
      <p>Op Car Free Day laat je de eigen auto thuis voor de trip van en naar het werk. Ontdek jouw voordelen van de (elektrische) fiets, het openbaar vervoer of carpoolen. Een fijne attentie op de werkvloer maakt het nog leuker.</p>
      <a class="btn small-button" href="#">Meer over Car Free Day</a>
    </article>
    <article class="event-day vrijdag">
      <p class="event-date">Vrijdag 21/9</p>
      <h3 class="actie-title">Strapdag</h3>
      <p>De kerndoelstelling van de Strapdag is het aanmoedigen van scholen, kinderen en ouders om zich duurzaam en veilig naar school te verplaatsen. Duurzame mobiliteit in het woon-schoolverkeer verdient permanente aandacht. Strapdag zet de leuke aspecten van stappen en trappen op een ludieke manier in de verf.</p>
